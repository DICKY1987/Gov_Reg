    def generate_drawio(self):
        """Generate DrawIO XML files"""
        print("\n=== Generating DrawIO Files ===\n")

        drawio_dir = self.output_dir / "drawio"
        workflows = self.index.get('by_workflow', {})

        for workflow_name in list(workflows.keys())[:5]:  # Generate for first 5 workflows
            self._generate_drawio_workflow(drawio_dir, workflow_name)

