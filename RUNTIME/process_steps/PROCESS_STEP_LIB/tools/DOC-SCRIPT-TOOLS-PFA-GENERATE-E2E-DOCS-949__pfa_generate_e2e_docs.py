#!/usr/bin/env python3
"""
E2E Documentation Generator
Generates comprehensive documentation from unified schema and master index.

Outputs:
1. Markdown documentation (README, phase guides, workflows, references)
2. Mermaid diagrams (state machines, workflows, component interactions)
3. DrawIO XML files (visual process maps with swimlanes)
4. HTML interactive view (searchable, filterable catalog)

Usage:
    python pfa_generate_e2e_docs.py --schema unified.yaml --index master_index.json --output-dir docs/e2e/ --formats markdown mermaid drawio html
"""
DOC_ID: DOC-SCRIPT-TOOLS-PFA-GENERATE-E2E-DOCS-949

import json
import yaml
import argparse
from pathlib import Path
from typing import Dict, List, Any, Optional
from collections import defaultdict
import sys

class E2EDocGenerator:
    """Generates multi-format documentation from unified schema and master index"""

    def __init__(self, schema_path: Path, index_path: Path, output_dir: Path):
        self.schema_path = schema_path
        self.index_path = index_path
        self.output_dir = output_dir

        # Load data
        self.schema = self._load_yaml(schema_path)
        self.index = self._load_json(index_path)

        # Extract metadata
        self.meta = self.schema.get('meta', {})
        self.steps = self.schema.get('steps', [])
        self.universal_phases = self.schema.get('universal_phases', [])

        print(f"Loaded schema: {self.meta.get('schema_id', 'Unknown')}")
        print(f"  Version: {self.meta.get('version', 'Unknown')}")
        print(f"  Steps: {len(self.steps)}")
        print(f"  Phases: {len(self.universal_phases)}")

    def _load_yaml(self, path: Path) -> Dict:
        """Load YAML file"""
        with open(path, 'r', encoding='utf-8') as f:
            return yaml.safe_load(f)

    def _load_json(self, path: Path) -> Dict:
        """Load JSON file"""
        with open(path, 'r', encoding='utf-8') as f:
            return json.load(f)

    def _save_file(self, path: Path, content: str):
        """Save file with directory creation"""
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"  ✓ Generated: {path.relative_to(self.output_dir)}")

    # ============================================================================
    # MARKDOWN GENERATION
    # ============================================================================

    def generate_markdown(self):
        """Generate all markdown documentation"""
        print("\n=== Generating Markdown Documentation ===\n")

        # Main README
        self._generate_readme()

        # Phase guides (9 files)
        self._generate_phase_guides()

        # Component reference
        self._generate_component_reference()

        # Operations reference
        self._generate_operations_reference()

        # Workflow guides (5+ files)
        self._generate_workflow_guides()

        # Phase mappings reference
        self._generate_phase_mappings()

    def _generate_readme(self):
        """Generate main README.md"""
        readme = f"""# E2E Process Map Documentation

**Schema:** {self.meta.get('schema_id', 'Unknown')}
**Version:** {self.meta.get('version', 'Unknown')}
**Generated:** {self.meta.get('last_updated', 'Unknown')}

---

## Overview

This documentation covers the complete end-to-end process map with **{len(self.steps)} steps** across **{len(self.universal_phases)} universal phases**.

### Source Schemas

{self._format_list(self.meta.get('source_schemas', []))}

### Merge Statistics

- **Total steps merged:** {self.meta.get('merge_statistics', {}).get('total_steps_merged', 'Unknown')}
- **Unique steps after dedup:** {self.meta.get('merge_statistics', {}).get('unique_steps_after_dedup', 'Unknown')}
- **Duplicates removed:** {self.meta.get('merge_statistics', {}).get('duplicates_removed', 'Unknown')}

---

## Universal Phases

{self._generate_phase_tree()}

---

## Navigation

### By Phase
{self._generate_phase_links()}

### By Component
- [Component Reference](components.md) - All {len(self.index.get('by_component', {}))} components

### By Operation
- [Operations Reference](operations.md) - All {len(self.index.get('by_operation', {}))} operation types

### By Workflow
{self._generate_workflow_links()}

---

## Statistics

- **Total Steps:** {len(self.steps)}
- **Phases:** {len(self.universal_phases)}
- **Components:** {len(self.index.get('by_component', {}))}
- **Operations:** {len(self.index.get('by_operation', {}))}
- **Workflows:** {len(self.index.get('by_workflow', {}))}

---

## Quick Links

- [Phase State Machine Diagram](diagrams/phase_state_machine.md)
- [Full E2E Workflow](workflows/full_e2e.md)
- [Component Interactions](diagrams/component_interactions.md)

---

**Generated by:** pfa_generate_e2e_docs.py
"""
        self._save_file(self.output_dir / "README.md", readme)

    def _generate_phase_tree(self) -> str:
        """Generate phase tree with step counts"""
        tree = []
        phase_index = self.index.get('by_phase', {})

        for phase_id in self.universal_phases:
            # Phase is just a string ID, not an object
            step_count = len(phase_index.get(phase_id, []))
            phase_name = phase_id.replace('_', ' ').title()

            tree.append(f"### {phase_name} ({step_count} steps)")
            tree.append(f"**Phase ID:** `{phase_id}`")
            tree.append("")

        return "\n".join(tree)

    def _generate_phase_links(self) -> str:
        """Generate links to phase guides"""
        links = []
        for phase_id in self.universal_phases:
            phase_name = phase_id.replace('_', ' ').title()
            links.append(f"- [{phase_name}](phases/{phase_id}.md)")
        return "\n".join(links)

    def _generate_workflow_links(self) -> str:
        """Generate links to workflow guides"""
        links = []
        workflows = self.index.get('by_workflow', {})
        for workflow_name in workflows.keys():
            links.append(f"- [{workflow_name.replace('_', ' ').title()}](workflows/{workflow_name}.md)")
        return "\n".join(links)

    def _format_list(self, items: List[str]) -> str:
        """Format list of items as markdown list"""
        return "\n".join(f"- {item}" for item in items)

    def _generate_phase_guides(self):
        """Generate individual phase guide files"""
        phases_dir = self.output_dir / "phases"
        phase_index = self.index.get('by_phase', {})

        for phase_id in self.universal_phases:
            phase_name = phase_id.replace('_', ' ').title()
            step_ids = phase_index.get(phase_id, [])
            steps = [s for s in self.steps if s.get('step_id') in step_ids]

            guide = f"""# {phase_name}

**Phase ID:** `{phase_id}`
**Steps:** {len(steps)}

---

## Overview

This phase contains {len(steps)} steps that handle {phase_name.lower()} activities.

---

## Steps in this Phase

{self._generate_step_table(steps)}

---

## Dependencies

{self._generate_phase_dependencies(phase_id)}

---

**Navigation:** [← Back to Index](../README.md) | [Next Phase →](../phases/{self._get_next_phase_id(phase_id)}.md)
"""
            self._save_file(phases_dir / f"{phase_id}.md", guide)

    def _generate_step_table(self, steps: List[Dict]) -> str:
        """Generate markdown table of steps"""
        if not steps:
            return "*No steps in this phase*"

        table = ["| Step ID | Name | Component | Operation |",
                 "|---------|------|-----------|-----------|"]

        for step in steps[:10]:  # Limit to first 10 for brevity
            step_id = step.get('step_id', 'Unknown')
            name = step.get('name', 'Unknown')[:40]
            component = step.get('responsible_component', 'Unknown')[:30]
            operation = step.get('operation_kind', 'Unknown')
            table.append(f"| {step_id} | {name} | {component} | {operation} |")

        if len(steps) > 10:
            table.append(f"| ... | *{len(steps) - 10} more steps* | ... | ... |")

        return "\n".join(table)



    def _generate_phase_dependencies(self, phase_id: str) -> str:
        """Generate phase dependency information"""
        # Simplified - could be enhanced with actual dependency analysis
        return "*Phase dependencies to be analyzed*"

    def _get_next_phase_id(self, current_phase_id: str) -> str:
        """Get next phase ID in sequence"""
        try:
            idx = self.universal_phases.index(current_phase_id)
            return self.universal_phases[idx + 1] if idx + 1 < len(self.universal_phases) else self.universal_phases[0]
        except ValueError:
            return self.universal_phases[0] if self.universal_phases else 'unknown'

    def _generate_component_reference(self):
        """Generate component reference documentation"""
        components = self.index.get('by_component', {})

        content = f"""# Component Reference

**Total Components:** {len(components)}

---

## Component Index

{self._generate_component_index(components)}

---

## Component Details

{self._generate_component_details(components)}
"""
        self._save_file(self.output_dir / "components.md", content)

    def _generate_component_index(self, components: Dict) -> str:
        """Generate component index"""
        index = []
        for component_name in sorted(components.keys()):
            step_count = len(components[component_name])
            index.append(f"- [{component_name}](#{component_name.replace('::', '-')}) - {step_count} steps")
        return "\n".join(index)

    def _generate_component_details(self, components: Dict) -> str:
        """Generate detailed component information"""
        details = []
        for component_name in sorted(components.keys()):
            step_ids = components[component_name]
            steps = [s for s in self.steps if s.get('step_id') in step_ids]

            details.append(f"### {component_name}")
            details.append(f"**Steps:** {len(steps)}")
            details.append("")
            details.append(self._generate_step_table(steps))
            details.append("")

        return "\n".join(details)

    def _generate_operations_reference(self):
        """Generate operations reference documentation"""
        operations = self.index.get('by_operation', {})

        content = f"""# Operations Reference

**Total Operation Types:** {len(operations)}

---

## Operation Index

{self._generate_operation_index(operations)}

---

## Operation Details

{self._generate_operation_details(operations)}
"""
        self._save_file(self.output_dir / "operations.md", content)

    def _generate_operation_index(self, operations: Dict) -> str:
        """Generate operation index"""
        index = []
        for operation in sorted(operations.keys()):
            step_count = len(operations[operation])
            index.append(f"- [{operation}](#{operation.replace('_', '-')}) - {step_count} steps")
        return "\n".join(index)

    def _generate_operation_details(self, operations: Dict) -> str:
        """Generate detailed operation information"""
        details = []
        for operation in sorted(operations.keys()):
            step_ids = operations[operation]

            details.append(f"### {operation}")
            details.append(f"**Steps using this operation:** {len(step_ids)}")
            details.append(f"**Step IDs:** {', '.join(step_ids[:5])}")
            if len(step_ids) > 5:
                details.append(f"... and {len(step_ids) - 5} more")
            details.append("")

        return "\n".join(details)

    def _generate_workflow_guides(self):
        """Generate workflow guide files"""
        workflows_dir = self.output_dir / "workflows"
        workflows = self.index.get('by_workflow', {})

        for workflow_name, step_ids in workflows.items():
            steps = [s for s in self.steps if s.get('step_id') in step_ids]

            guide = f"""# {workflow_name.replace('_', ' ').title()} Workflow

**Workflow ID:** {workflow_name}
**Steps:** {len(steps)}

---

## Workflow Overview

*Workflow description to be added*

---

## Workflow Steps

{self._generate_workflow_step_list(steps)}

---

## Workflow Diagram

See [workflow diagram](../diagrams/workflow_{workflow_name}.md) for visual representation.

---

**Navigation:** [← Back to Index](../README.md)
"""
            self._save_file(workflows_dir / f"{workflow_name}.md", guide)

    def _generate_workflow_step_list(self, steps: List[Dict]) -> str:
        """Generate numbered list of workflow steps"""
        if not steps:
            return "*No steps in this workflow*"

        step_list = []
        for i, step in enumerate(steps, 1):
            step_id = step.get('step_id', 'Unknown')
            name = step.get('name', 'Unknown')
            step_list.append(f"{i}. **{step_id}** - {name}")

        return "\n".join(step_list)

    def _generate_phase_mappings(self):
        """Generate phase mappings reference"""
        content = """# Phase Mappings Reference

**Original Phases → Universal Phases**

This document shows how original phase names from source schemas map to the 9 universal phases.

---

## Mapping Table

*Phase mappings to be extracted from schema*

---

**Navigation:** [← Back to Index](README.md)
"""
        self._save_file(self.output_dir / "phase_mappings.md", content)

